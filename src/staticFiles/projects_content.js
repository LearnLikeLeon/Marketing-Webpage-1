export async function getAllCompletedDetailedProjects() {
  // Sort completed_projects by date in descending order
  return [...completed_detailed_projects].sort(
    (a, b) => new Date(b.date).getTime() - new Date(a.date).getTime()
  );
}

export async function getProjectContentBySlug(slug) {
  return completed_detailed_projects.find(
    (each_project) => each_project.slug === slug
  );
}
